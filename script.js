/**
 * テキスト内のビジネス用語をローカルの辞書から解説します。
 * @param {string} text 解説を依頼するテキスト
 * @returns {string} 解説文
 */
async function explainBusinessTerms(text) {
    let explanation = '';
    let foundTermsCount = 0;
    const maxTerms = 5; // 最大5つまで解説

    // businessTermsオブジェクトのキー（用語）をループ
    for (const term in businessTerms) {
        if (businessTerms.hasOwnProperty(term)) {
            // テキスト内に用語が含まれているかチェック（大文字小文字を区別しない）
            const regex = new RegExp(term, 'gi');
            if (regex.test(text)) {
                if (foundTermsCount < maxTerms) {
                    explanation += `**${term}**: ${businessTerms[term]}
`;
                    foundTermsCount++;
                } else {
                    break; // 最大数に達したらループを抜ける
                }
            }
        }
    }

    if (explanation === '') {
        return '解説対象の用語が見つかりませんでした。';
    } else {
        return explanation;
    }
}

